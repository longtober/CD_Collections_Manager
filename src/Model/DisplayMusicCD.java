    private void loadDataForTable() {
        tblManager.setRowCount(0);
        for (int i = 0; i < musicdb.getSize(); i++) {
            MusicCD currentCD = musicdb.get(i);
            tblManager.addRow(new Object[]{i + 1, currentCD.getCode(), currentCD.getTitle(), currentCD.getArtist(), currentCD.getPrice()});
        }
    }

