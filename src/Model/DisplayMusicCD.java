    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String code = tfCode.getText().trim();
        int pos = musicdb.searchByCode(code);
        if (pos != -1) {
            MusicCD currentCD = musicdb.get(pos);
            tblManager.setRowCount(0);
            tblManager.addRow(new Object[]{1, currentCD.getCode(), currentCD.getTitle(), currentCD.getArtist(), currentCD.getPrice()});
            warningCodeNotFound(false);
        } else {
            warningCodeNotFound(true);
            if (tblManager.getRowCount() < 2) {
                loadDataForTable();
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void warningCodeNotFound(boolean status) {
        if (status) {
            lbWarning.setText("Code not found!");
        } else {
            lbWarning.setText("");
        }
    }

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String code = tfCode.getText().trim();
        int pos = musicdb.searchByCode(code);
        if (pos != -1) {
            MusicCD currentCD = musicdb.get(pos);
            musicdb.removeItemAt(pos);
            loadDataForTable();
            warningCodeNotFound(false);
        } else {
            warningCodeNotFound(true);
            if (tblManager.getRowCount() < 2) {
                loadDataForTable();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

